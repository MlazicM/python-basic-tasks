# This is first method to print sum of integers from 1 to 10.
print(sum(range(1, 11)))
# This program calculates and prints the sum of integers from 1 to 10.
# This is second method to print sum of integers from 1 to 10.
suma = 0
for i in range(1, 11):
    suma += i
print(suma)
# The first method is more concise and leverages built-in functions, while the second method uses an explicit loop to accumulate the sum.Both methods yield the same result.
# Both methods are valid and correct for calculating the sum of integers from 1 to 10.
